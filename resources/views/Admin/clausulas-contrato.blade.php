<div class="clausulas-title">Cláusulas</div>

<div class="clausulas-body">

    <p>
        <span class="clausula-tag">PRIMERA.</span>
        LA ARRENDADORA entrega en arrendamiento a la ARRENDATARIA cuyo nombre aparece en la carátula de este documento y dicha ARRENDATARIA recibe en tal carácter el vehículo objeto de este contrato en condiciones normales, mecánicas y de carrocería, consignadas en el inventario respectivo, con el carácter de BIEN ARRENDADO, a tener bajo su custodia y a su entera satisfacción, el vehículo de referencia y se obliga a pagar a la ARRENDADORA la renta señalada del contrato y a precisar de mercado, el o los faltantes de accesorios y partes del vehículo que recibe en el momento de entrega del mismo.
    </p>

    <p>
        <span class="clausula-tag">SEGUNDA.</span>
        El término forzoso de este contrato de arrendamiento está señalado en la carátula de este contrato y nunca podrá ser prorrogado por ninguna de las partes, sin que aparezca constancia de voluntad de los mismos. en un nuevo contrato de arrendamiento.
    </p>

    <p>
        <span class="clausula-tag">TERCERA.</span>
        LA ARRENDATARIA pagará como precio del arrendamiento el anticipo y precisamente el lugar donde deberán ser pagadas las cantidades estipuladas en el contrato de arrendamiento. Los pagos serán efectuados conforme a lo indicado en la carátula de este contrato. La renta deberá ser totalmente pagada aun cuando el vehículo se encuentre en uso de LA ARRENDATARIA, desde este momento, en plena posesión del automóvil y hasta la fecha en que lo reciba en devolución, a su entera satisfacción. LA ARRENDADORA.
    </p>

    <p>
        <span class="clausula-tag">CUARTA.</span>
        LA ARRENDATARIA se obliga a entregar en devolución el vehículo arrendado precisamente en la hora y fecha convenidas y en la oficina de la ARRENDADORA en que se hubiera pactado la devolución, apareciendo esos datos en la carátula de este contrato de la misma que el vehículo se encontrará lavado y en condiciones normales, siendo el vehículo devuelto con el tanque lleno de gasolina. LA ARRENDATARIA deberá devolverlo en el lugar indicado en la carátula de este contrato. LA ARRENDATARIA deberá devolver el vehículo al lugar convenido en el contrato y en el plazo estipulado, más el importe que corresponda si la arrendataria del tiempo normal de traslado del lugar donde LA ARRENDATARIA haya dejado el vehículo a la oficina donde debió entregarlo de acuerdo con este contrato, aplicándose en todo caso la cuota diaria.
    </p>

    <p>
        <span class="clausula-tag">QUINTA.</span>
        LA ARRENDATARIA tal como antes se señala se obliga a entregar el vehículo arrendado al término de este contrato, con el solo desgaste del uso normal y moderado, precisamente en la fecha y hora convenida y saldando en la carátula del contrato. con el pago del arrendamiento y en las condiciones señaladas en el contrato.
    </p>

    <p>
        <span class="clausula-tag">SEXTA.</span>
        En caso de que LA ARRENDADORA niegue cualquier diligencia, previa autorización del pago de las prestaciones debidas por LA ARRENDATARIA o bien obtenga el vehículo devuelto legalmente aplicando las medidas de orden judicial o por acuerdo entre las partes, se autoriza LA ARRENDADORA para disponer del vehículo en la forma que estime más adecuada, ya sea. venderlo, arrendarlo o cualquier otra forma de disposición que convenga a los intereses de LA ARRENDADORA.
    </p>

    <p>
        <span class="clausula-tag">SÉPTIMA.</span>
        LA ARRENDATARIA se obliga a mantener el vehículo en buenas condiciones, realizando los servicios de mantenimiento preventivo y correctivo necesarios para su buen funcionamiento y conservación, así como a cubrir los gastos derivados de su uso normal.
    </p>

    <p>
        <span class="clausula-tag">OCTAVA.</span>
        El vehículo arrendado se destinará única y exclusivamente al transporte de LA ARRENDATARIA y sus acompañantes, y solo podrá ser manejado por LA ARRENDATARIA o por conductores autorizados que cuenten con licencia vigente. Queda prohibido usar el vehículo para fines distintos a los pactados.
    </p>

    <p>
        <span class="clausula-tag">NOVENA.</span>
        El vehículo arrendado no podrá ser conducido fuera de los límites del territorio de la República Mexicana, sin el previo consentimiento expreso y por escrito de LA ARRENDADORA.
    </p>

    <p>
        <span class="clausula-tag">DÉCIMA.</span>
        LA ARRENDATARIA se obliga a no permitir que el vehículo sea utilizado para actividades ilícitas o contrarias a la ley.
    </p>

    <p>
        <span class="clausula-tag">DÉCIMA PRIMERA.</span>
        LA ARRENDATARIA será responsable de cualquier daño. desperfecto o pérdida total o parcial del vehículo durante la vigencia del presente contrato. aun cuando sea causado por terceros.
    </p>

    <p>
        <span class="clausula-tag">DÉCIMA SEGUNDA.</span>
        En caso de accidente, robo o pérdida total del vehículo. LA ARRENDATARIA deberá dar aviso inmediato a LA ARRENDADORA y a las autoridades correspondientes. obligándose a cubrir los daños y perjuicios conforme a lo estipulado en este contrato.
    </p>

    <p>
        <span class="clausula-tag">DÉCIMA TERCERA.</span>
        LA ARRENDATARIA no podrá subarrendar. prestar. ceder o permitir el uso del vehículo a terceros sin autorización previa y por escrito de LA ARRENDADORA.
    </p>

    <p>
        <span class="clausula-tag">DÉCIMA CUARTA.</span>
        LA ARRENDATARIA se obliga a pagar todas las multas, infracciones, gastos de arrastre. corralón y cualquier otro cargo que se genere por el uso del vehículo durante la vigencia del contrato.
    </p>

    <p>
        <span class="clausula-tag">DÉCIMA QUINTA.</span>
        LA ARRENDATARIA deberá cubrir el importe de los daños ocasionados al vehículo por negligencia. imprudencia o mal uso del mismo.
    </p>

    <p>
        <span class="clausula-tag">DÉCIMA SEXTA.</span>
        LA ARRENDADORA no será responsable por los objetos personales dejados dentro del vehículo arrendado durante el tiempo que se encuentre en posesión de LA ARRENDATARIA.
    </p>

    <p>
        <span class="clausula-tag">DÉCIMA SÉPTIMA.</span>
        LA ARRENDATARIA reconoce que ha recibido el vehículo en óptimas condiciones y se obliga a devolverlo en el mismo estado, salvo el desgaste normal por el uso.
    </p>

    <p>
        <span class="clausula-tag">DÉCIMA OCTAVA.</span>
        En caso de incumplimiento de cualquiera de las obligaciones establecidas en el presente contrato. LA ARRENDADORA podrá darlo por rescindido sin necesidad de declaración judicial.
    </p>

    <p>
        <span class="clausula-tag">DÉCIMA NOVENA.</span>
        Para la interpretación y cumplimiento del presente contrato. las partes se someten a la jurisdicción de los tribunales competentes del Estado de Querétaro. renunciando a cualquier otro fuero que pudiera corresponderles por razón de su domicilio presente o futuro.
    </p>

    <p>
        <span class="clausula-tag">VIGÉSIMA.</span>
        Las partes manifiestan que conocen y aceptan todas y cada una de las cláusulas del presente contrato. firmándolo de conformidad.
    </p>

